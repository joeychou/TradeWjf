        /// <summary>
        /// 执行操作类（Insert/Delete/Update）存储过程。
        /// </summary>
        /// <param name="storeProceName">存储过程名</param>
        /// <param name="paraValues">传递给存储过程的参数值列表</param>
        /// <returns></returns>
        public static int ExecuteNonQuery(string storeProceName, params object[] paraValues)
        {
            using (MySqlConnection connection = new MySqlConnection(connectionString))
            {
                MySqlCommand command = new MySqlCommand(storeProceName, connection);
                command.CommandType = CommandType.StoredProcedure;
                try
                {
                    MySqlCommandBuilder.DeriveParameters(command);
                    if (paraValues != null)
                    {
                        if ((command.Parameters.Count - 1) != paraValues.Length)
                        {
                            throw new ArgumentNullException("The number of parameters does not match number of values for stored procedure.");
                        }
                        for (int i = 0; i < paraValues.Length; i++)
                        {
                            command.Parameters[i + 1].Value = (paraValues[i] == null) ? DBNull.Value : paraValues[i];
                        }
                    }
                    connection.Open();
                    int affectedRowsCount = command.ExecuteNonQuery();
                    return affectedRowsCount;
                }
                catch (MySql.Data.MySqlClient.MySqlException e)
                {
                    connection.Close();
                    throw e;
                }
            }
        }
